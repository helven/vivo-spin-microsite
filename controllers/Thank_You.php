<?php
Class Thank_You extends Z_Controller
{
/* 
| ------------------------------------------------------------------------------------------------------------------------------------------
| MAGIC FUNCTIONS
| ------------------------------------------------------------------------------------------------------------------------------------------
*/
	function __construct()
	{
		parent::__construct();
	}
/* 
| ------------------------------------------------------------------------------------------------------------------------------------------
| PUBLIC function
| ------------------------------------------------------------------------------------------------------------------------------------------
*/
	function index()
	{
		if(!isset($_SESSION['ss_Submission']) && $this->config['environment'] == 'live' && $_GET['test_mode'] != 'true')
		{
			redirect(base_url());
		}
		// ----------------------------------------------------------------------- //
		// INIT
		// ----------------------------------------------------------------------- //
		$this->geoCountryCode	= (in_array(strtolower($_SESSION['ss_Geo']['country_code']), array('my', 'sg')))?$_SESSION['ss_Geo']['country_code']:'MY';
		$this->bitlyURL			= $this->config[strtolower($this->geoCountryCode)]['bitly'];
		
		// ----------------------------------------------------------------------- //
		// LOAD views and render
		// ----------------------------------------------------------------------- //
		$this->p_render('thank_you/index');
	}
/* 
| ------------------------------------------------------------------------------------------------------------------------------------------
| PRIVATE function
| ------------------------------------------------------------------------------------------------------------------------------------------
*/
	
}